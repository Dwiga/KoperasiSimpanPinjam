    private void masuktabel(){
            try{
                sta = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
                tbm = new DefaultTableModel();
                tbm.addColumn("ID Petugas");
                tbm.addColumn("Nama");
                tbm.addColumn("Alamat");
                tbm.addColumn("Telepon");
                tbm.addColumn("Tempat Lahir");
                tbm.addColumn("Tanggal Lahir");
                tbm.addColumn("Keterangan");
                
                jTable1.setModel(tbm);
                rs = sta.executeQuery("SELECT * FROM petugas_koperasi");
                while (rs.next()){
                    tbm.addRow(new Object[]{
                    rs.getString("id_petugas"),
                    rs.getString("nama"),
                    rs.getString("alamat"),
                    rs.getString("no_telp"),
                    rs.getString("temp_lahir"),
                    rs.getString("tgl_lahir"),
                    rs.getString("ket"),
                });
                    jTable1.setModel(tbm);
                    }
                }catch(Exception e){
                        JOptionPane.showMessageDialog(rootPane,"Gagal"+e);
                        
                    }
    }   
