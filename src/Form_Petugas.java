    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        if (txtalamat.getText().equals("")||
            txtnama.getText().equals("")||
            txttempatlahir.getText().equals("")||
            txtnotelp.getText().equals("")
        )
        {
            JOptionPane.showMessageDialog(rootPane, "Isi Terlebih Dahulu Semua Data \n");

        } else {
            try {
                String sql;
                sql = ("INSERT INTO petugas_koperasi(nama,alamat,no_telp,temp_lahir,tgl_lahir,ket) VALUES('"+ txtnama.getText() +"','"+ txtalamat.getText() +"','"+ txtnotelp.getText() +"','"+ txttempatlahir.getText() +"','"+ tanggal +"','-')");
                sta.executeUpdate(sql);
                JOptionPane.showMessageDialog(rootPane, "BERHASIL");
                this.dispose();
                new Form_Petugas().setVisible(true);
            } catch (Exception e) {
            }

        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btntambahActionPerformed

